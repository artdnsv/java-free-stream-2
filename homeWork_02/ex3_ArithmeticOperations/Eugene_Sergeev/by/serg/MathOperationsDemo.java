package by.serg;

public class MathOperationsDemo{

    public static void main(String[] args) {
        System.out.println("Welcome to the 'Math Operations Demo'!");
        int a = 60;
        int b = 30;

        System.out.println("1) Addition '+':");
        System.out.print("a = ");
        System.out.print(a);
        System.out.print(", b = ");
        System.out.println(b);

        int sum = a + b;

        System.out.print("a + b = ");
        System.out.print(a);
        System.out.print(" + ");
        System.out.print(b);
        System.out.print(" = ");
        System.out.println(sum);

        System.out.println("2) Subtraction '-':");
        System.out.print("a = ");
        System.out.print(a);
        System.out.print(", b = ");
        System.out.println(b);

        int diff = a - b;

        System.out.print("a - b = ");
        System.out.print(a);
        System.out.print(" - ");
        System.out.print(b);
        System.out.print(" = ");
        System.out.println(diff);

        System.out.println("3) Multiplication '*':");
        System.out.print("a = ");
        System.out.print(a);
        System.out.print(", b = ");
        System.out.println(b);

        int mult = a * b;

        System.out.print("a * b = ");
        System.out.print(a);
        System.out.print(" * ");
        System.out.print(b);
        System.out.print(" = ");
        System.out.println(mult);

        System.out.println("4) Division '/':");
        System.out.print("a = ");
        System.out.print(a);
        System.out.print(", b = ");
        System.out.println(b);

        int div = a / b;

        System.out.print("a / b = ");
        System.out.print(a);
        System.out.print(" / ");
        System.out.print(b);
        System.out.print(" = ");
        System.out.print(div);
    }
}